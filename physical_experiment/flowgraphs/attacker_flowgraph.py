#!/usr/bin/env python3
"""
GNU Radio Attacker Flowgraph - Sniffer and Replayer

Generated by grc_generator.py
Sniffs legitimate traffic and replays frames.

Architecture:
    [HackRF Source (RX)] --> [Demod] --> [ZMQ PUSH] --> Python (attacker_relay.py)
                                                              |
    [HackRF Sink (TX)] <-- [Mod] <-- [ZMQ PULL] <-------------+

Note: HackRF is half-duplex. This flowgraph switches between RX and TX modes.
For simultaneous operation, use two HackRFs.
"""
from gnuradio import gr, blocks, digital, filter as gr_filter, analog
from gnuradio.filter import firdes
import osmosdr
import sys
import time
import threading

class AttackerFlowgraph(gr.top_block):
    def __init__(
        self,
        frequency=2475000000.0,  # 2.475 GHz (论文3.3节)
        sample_rate=2000000.0,
        lna_gain=24,
        tx_gain=20,
        rx_zmq_port=5557,
        tx_zmq_port=5558,
        baud_rate=9600,
        bind_host="127.0.0.1",
    ):
        gr.top_block.__init__(self, "IoT Replay Attacker")

        self.frequency = frequency
        self.sample_rate = sample_rate
        self.baud_rate = baud_rate
        self.samples_per_symbol = int(sample_rate / baud_rate)

        ##################################################
        # RX Path (Sniffer)
        ##################################################

        # HackRF Source
        self.hackrf_source = osmosdr.source(args="numchan=1")
        self.hackrf_source.set_sample_rate(sample_rate)
        self.hackrf_source.set_center_freq(frequency, 0)
        self.hackrf_source.set_gain(lna_gain, 0)
        self.hackrf_source.set_if_gain(20, 0)

        # Demodulation chain
        lpf_taps = firdes.low_pass(1.0, sample_rate, baud_rate * 2, baud_rate * 0.5)
        self.lpf = gr_filter.fir_filter_ccf(1, lpf_taps)

        sensitivity = sample_rate / (2 * 3.14159 * 5000)
        self.fm_demod = analog.quadrature_demod_cf(sensitivity)

        self.clock_recovery = digital.clock_recovery_mm_ff(
            self.samples_per_symbol, 0.25 * 0.175**2, 0.5, 0.175, 0.005
        )
        self.slicer = digital.binary_slicer_fb()
        self.pack = blocks.pack_k_bits_bb(8)

        # ZMQ Push (to attacker script)
        self.zmq_rx_sink = blocks.zeromq_push_sink(
            gr.sizeof_char, 1, f"tcp://{bind_host}:{rx_zmq_port}", 100, True, -1
        )

        ##################################################
        # TX Path (Replayer) - Commented out for half-duplex
        ##################################################
        # Uncomment these if using two HackRFs

        # self.zmq_tx_source = blocks.zeromq_pull_source(
        #     gr.sizeof_char, 1, f"tcp://127.0.0.1:{tx_zmq_port}", 100, True, -1
        # )
        # ... modulation chain ...
        # self.hackrf_sink = osmosdr.sink(args="numchan=1 hackrf=1")

        ##################################################
        # Connections (RX only for half-duplex)
        ##################################################
        self.connect(self.hackrf_source, self.lpf)
        self.connect(self.lpf, self.fm_demod)
        self.connect(self.fm_demod, self.clock_recovery)
        self.connect(self.clock_recovery, self.slicer)
        self.connect(self.slicer, self.pack)
        self.connect(self.pack, self.zmq_rx_sink)

        print(f"Attacker Flowgraph (Sniffer) initialized:")
        print(f"  Frequency: {frequency/1e6:.3f} MHz")
        print(f"  RX ZMQ Port: {rx_zmq_port}")
        print(f"  TX ZMQ Port: {tx_zmq_port} (for replay script)")


def main():
    import argparse
    parser = argparse.ArgumentParser()
    parser.add_argument("--freq", type=float, default=433920000.0)
    parser.add_argument("--rate", type=float, default=2000000.0)
    parser.add_argument("--lna-gain", type=float, default=24)
    parser.add_argument("--tx-gain", type=float, default=20)
    parser.add_argument("--rx-zmq-port", type=int, default=5557)
    parser.add_argument("--tx-zmq-port", type=int, default=5558)
    parser.add_argument("--bind-host", type=str, default="127.0.0.1")
    args = parser.parse_args()

    tb = AttackerFlowgraph(
        frequency=args.freq,
        sample_rate=args.rate,
        lna_gain=args.lna_gain,
        tx_gain=args.tx_gain,
        rx_zmq_port=args.rx_zmq_port,
        tx_zmq_port=args.tx_zmq_port,
        bind_host=args.bind_host,
    )

    print("Starting Attacker flowgraph (sniffing)... Press Ctrl+C to stop")
    tb.start()

    try:
        while True:
            time.sleep(1)
    except KeyboardInterrupt:
        pass

    tb.stop()
    tb.wait()
    print("Attacker flowgraph stopped")


if __name__ == "__main__":
    main()
