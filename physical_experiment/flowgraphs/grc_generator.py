def generate_flowgraph(
    flowgraph_type: str,
    frequency: float = 433.92e6,
    sample_rate: float = 2e6,
    baud_rate: int = 9600,
    tx_gain: float = 20,
    if_gain: float = 30,
    lna_gain: float = 24,
    vga_gain: float = 20,
    zmq_port: int = 5555,
    rx_zmq_port: int = 5557,
    tx_zmq_port: int = 5558,
    bind_host: str = "127.0.0.1",
) -> str:
    """Generate flowgraph Python code."""

    if flowgraph_type == "tx":
        template = TX_FLOWGRAPH_TEMPLATE
        code = template.format(
            frequency=frequency,
            sample_rate=sample_rate,
            baud_rate=baud_rate,
            tx_gain=tx_gain,
            if_gain=if_gain,
            zmq_port=zmq_port
        )
    elif flowgraph_type == "rx":
        template = RX_FLOWGRAPH_TEMPLATE
        code = template.format(
            frequency=frequency,
            sample_rate=sample_rate,
            baud_rate=baud_rate,
            lna_gain=lna_gain,
            vga_gain=vga_gain,
            zmq_port=zmq_port,
            bind_host=bind_host,
        )
    elif flowgraph_type == "attacker":
        template = ATTACKER_FLOWGRAPH_TEMPLATE
        code = template.format(
            frequency=frequency,
            sample_rate=sample_rate,
            baud_rate=baud_rate,
            lna_gain=lna_gain,
            tx_gain=tx_gain,
            rx_zmq_port=rx_zmq_port,
            tx_zmq_port=tx_zmq_port,
            bind_host=bind_host,
        )
    else:
        raise ValueError(f"Unknown flowgraph type: {flowgraph_type}")

    return code


def main():
    parser = argparse.ArgumentParser(
        description="Generate GNU Radio flowgraphs for hardware experiments"
    )

    parser.add_argument("type", choices=["tx", "rx", "attacker", "all"],
                        help="Flowgraph type to generate")
    parser.add_argument("--output", "-o", type=str,
                        help="Output file path (default: stdout or auto-named)")
    parser.add_argument("--output-dir", type=str, default=".",
                        help="Output directory for 'all' mode")
    parser.add_argument("--freq", type=float, default=433.92e6,
                        help="Center frequency in Hz")
    parser.add_argument("--rate", type=float, default=2e6,
                        help="Sample rate in Hz")
    parser.add_argument("--baud", type=int, default=9600,
                        help="Symbol rate")
    parser.add_argument("--bind-host", type=str, default="127.0.0.1",
                        help="Host/IP for generated ZMQ bind sockets")
    parser.add_argument("--run", action="store_true",
                        help="Run the flowgraph after generating")

    args = parser.parse_args()

    if args.type == "all":
        # Generate all flowgraphs
        output_dir = Path(args.output_dir)
        output_dir.mkdir(parents=True, exist_ok=True)

        for fg_type in ["tx", "rx", "attacker"]:
            code = generate_flowgraph(
                fg_type,
                frequency=args.freq,
                sample_rate=args.rate,
                baud_rate=args.baud,
                bind_host=args.bind_host,
            )
            output_file = output_dir / f"{fg_type}_flowgraph.py"
            with open(output_file, 'w') as f:
                f.write(code)
            print(f"Generated: {output_file}")

    else:
        code = generate_flowgraph(
            args.type,
            frequency=args.freq,
            sample_rate=args.rate,
            baud_rate=args.baud,
            bind_host=args.bind_host,
        )

        if args.output:
            with open(args.output, 'w') as f:
                f.write(code)
            print(f"Generated: {args.output}")

            if args.run:
                import subprocess
                subprocess.run([sys.executable, args.output])
        else:
            print(code)


if __name__ == "__main__":
    main()
