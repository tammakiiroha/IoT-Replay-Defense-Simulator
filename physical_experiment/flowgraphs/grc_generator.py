class RxFlowgraph(gr.top_block):
    def __init__(
        self,
        frequency={frequency},
        sample_rate={sample_rate},
        lna_gain={lna_gain},
        vga_gain={vga_gain},
        zmq_port={zmq_port},
        baud_rate={baud_rate},
        bind_host="{bind_host}"
    ):
        gr.top_block.__init__(self, "IoT Replay Defense RX")

        ##################################################
        # Variables
        ##################################################
        self.frequency = frequency
        self.sample_rate = sample_rate
        self.lna_gain = lna_gain
        self.vga_gain = vga_gain
        self.baud_rate = baud_rate
        self.samples_per_symbol = int(sample_rate / baud_rate)

        ##################################################
        # Blocks
        ##################################################

        # HackRF Source
        self.hackrf_source = osmosdr.source(args="numchan=1")
        self.hackrf_source.set_sample_rate(sample_rate)
        self.hackrf_source.set_center_freq(frequency, 0)
        self.hackrf_source.set_freq_corr(0, 0)
        self.hackrf_source.set_dc_offset_mode(0, 0)
        self.hackrf_source.set_iq_balance_mode(0, 0)
        self.hackrf_source.set_gain_mode(False, 0)
        self.hackrf_source.set_gain(lna_gain, 0)
        self.hackrf_source.set_if_gain(vga_gain, 0)
        self.hackrf_source.set_bb_gain(20, 0)
        self.hackrf_source.set_antenna("", 0)
        self.hackrf_source.set_bandwidth(0, 0)

        # Low pass filter
        lpf_cutoff = baud_rate * 2
        lpf_transition = baud_rate * 0.5
        self.lpf_taps = firdes.low_pass(
            1.0, sample_rate, lpf_cutoff, lpf_transition,
            firdes.WIN_HAMMING
        )
        self.lpf = gr_filter.fir_filter_ccf(1, self.lpf_taps)

        # FM demodulation (for FSK)
        self.deviation = 5000  # Hz
        gain = sample_rate / (2 * 3.14159 * self.deviation)
        self.fm_demod = analog.quadrature_demod_cf(gain)

        # Clock recovery
        omega = self.samples_per_symbol
        gain_omega = 0.25 * 0.175 * 0.175  # (gain_mu)^2 / 4
        self.clock_recovery = digital.clock_recovery_mm_ff(
            omega,           # omega
            gain_omega,      # gain_omega
            0.5,             # mu
            0.175,           # gain_mu
            0.005            # omega_relative_limit
        )

        # Binary slicer
        self.slicer = digital.binary_slicer_fb()

        # Correlate access code (preamble detection)
        # Access code: 0xA5A5A5A5 = 10100101 10100101 10100101 10100101
        self.access_code = "10100101101001011010010110100101"
        self.correlator = digital.correlate_access_code_tag_bb(
            self.access_code,
            0,              # threshold
            "packet_start"  # tag name
        )

        # Pack bits to bytes
        self.pack = blocks.pack_k_bits_bb(8)

        # ZMQ Sink - Sends frames to Python script
        self.zmq_push_sink = blocks.zeromq_push_sink(
            gr.sizeof_char, 1,
            f"tcp://{{bind_host}}:{{zmq_port}}",
            100,  # timeout
            True, # pass_tags
            -1    # hwm
        )

        ##################################################
        # Connections
        ##################################################
        self.connect(self.hackrf_source, self.lpf)
        self.connect(self.lpf, self.fm_demod)
        self.connect(self.fm_demod, self.clock_recovery)
        self.connect(self.clock_recovery, self.slicer)
        self.connect(self.slicer, self.correlator)
        self.connect(self.correlator, self.pack)
        self.connect(self.pack, self.zmq_push_sink)

        print(f"RX Flowgraph initialized:")
        print(f"  Frequency: {{frequency/1e6:.3f}} MHz")
        print(f"  Sample Rate: {{sample_rate/1e6:.1f}} Msps")
        print(f"  ZMQ Port: {{zmq_port}}")


